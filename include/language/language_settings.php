function lang_form_custom_title($form_title) {
	$return_title = "";
	if($form_title == "sell") {
		$return_title = "Sell Page";
	} elseif($form_title == "home") {
		$return_title = "Home Page";
	} elseif($form_title == "model_details") {
		$return_title = "Model Details Page";
	} elseif($form_title == "cart") {
		$return_title = "Cart Page";
	} elseif($form_title == "checkout") {
		$return_title = "Checkout Page";
	} elseif($form_title == "order_completion") {
		$return_title = "Order Completion Page";
	} elseif($form_title == "reviews") {
		$return_title = "Reviews Page";
	} elseif($form_title == "offers") {
		$return_title = "Offers Page";
	} else {
		$return_title = $form_title;
	}
	return replace_us_to_space($return_title);
}

